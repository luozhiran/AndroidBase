package com.yk.androidbase.datas;

public class NewsCategoryData {
    private String name;
    private int a;
    private double b;

    public NewsCategoryData data;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getA() {
        return a;
    }

    public void setA(int a) {
        this.a = a;
    }

    public double getB() {
        return b;
    }

    public void setB(double b) {
        this.b = b;
    }

    public NewsCategoryData getData() {
        return data;
    }

    public void setData(NewsCategoryData data) {
        this.data = data;
    }
}
